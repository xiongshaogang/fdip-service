package com.trusdom.fdip.vo.mcip;

import com.trusdom.fdip.common.Json;

public class RedemptionVo {
	private String capitaloutstate;

	private String capitalserial;

	private String applyserial;

	public String getCapitaloutstate() {
		return capitaloutstate;
	}

	public void setCapitaloutstate(String capitaloutstate) {
		this.capitaloutstate = capitaloutstate;
	}

	public String getCapitalserial() {
		return capitalserial;
	}

	public void setCapitalserial(String capitalserial) {
		this.capitalserial = capitalserial;
	}

	public String getApplyserial() {
		return applyserial;
	}

	public void setApplyserial(String applyserial) {
		this.applyserial = applyserial;
	}

	@Override
	public String toString() {
		return Json.toJson(this).toString();
	}
}
